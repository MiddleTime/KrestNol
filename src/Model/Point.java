package Model;

import java.util.Random;

/**
 *
 * @author inbox_bv8awe9
 */
public class Point {
    private int x;
    private int y;

    public Point() {
    }

    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }

    
    
    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }
    
    public static Point getRandom(){
        return new Point(new Random().nextInt(3), new Random().nextInt(3));
    }
}
